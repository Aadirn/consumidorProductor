
package modelo;

public class Pasteles {
    
    private int peso;
    private String idPastel;

    public Pasteles(int peso, String id) {
        this.peso = peso;
        this.idPastel = id;
    }

    public int getPeso() {
        return peso;
    }

    public String getIdPastel() {
        return idPastel;
    }
    
    
    
}
